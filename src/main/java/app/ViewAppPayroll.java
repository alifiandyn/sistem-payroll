    private void masukanKomponenGaji(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_masukanKomponenGaji
        // TODO add your handling code here:
        Vector row = new Vector();
        ModelGajiPegawai komponenGaji = (ModelGajiPegawai) cbKomponenGaji.getSelectedItem();
        row.add(no++);
        row.add(komponenGaji.GetKomponenGajiPegawai());
        row.add(inputJumlahKomponenGaji.getText());
        Double komponen = Double.parseDouble(inputJumlahKomponenGaji.getText());
        this.totalHarga = this.totalHarga+komponen;
        tableModel.addRow(row);
        inputJumlahKomponenGaji.setText("0");
        cbKomponenGaji.setSelectedIndex(0);
    }//GEN-LAST:event_masukanKomponenGaji

    private void hapusKomponenGaji(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusKomponenGaji
        // TODO add your handling code here:
        int row = tablePayroll.getSelectedRow();
        Double totalHargaLama = (Double) tablePayroll.getValueAt(row, 2);
        this.totalHarga = this.totalHarga-totalHargaLama;
        tableModel.removeRow(tablePayroll.getSelectedRow());
        for(int i = 0; i < tableModel.getRowCount(); i++){
            tableModel.setValueAt(i+1,1,0);
            no = tableModel.getRowCount()+1;
        }
    }//GEN-LAST:event_hapusKomponenGaji

