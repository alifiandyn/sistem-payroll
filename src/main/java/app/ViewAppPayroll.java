    public String getData(){
        // TODO add your handling code here:
        String nipKaryawan = inputNIPKaryawan.getText();
        String namaKaryawan = inputNamaKaryawan.getText();
        String noTelepon = inputNoTelepon.getText();
        Date tanggalLahir = inputTanggalLahir.getDate();
        String tempatLahir = inputTempatLahir.getText();
        String alamat = inputAlamat.getText();
        
        String message = "NIP Karyawan : " + nipKaryawan + "\nNama Karyawan : " + namaKaryawan + "\n No. Telepon : " + noTelepon + "\nTempat, Tanggal Lahir : " + tempatLahir+", "+ tanggalLahir+"\nAlamat : "+ alamat+"\nDetail Pendapatan : \n";
        
        for (int i =0;i<tableModel.getRowCount();i++){
            for(int j = 0;j<=2;j++){
                if(j==0){
                    message += (tableModel.getValueAt(i,j)+". ");
                }else if(j==1){
                    message += (tableModel.getValueAt(i,j)+" : ");
                }else{
                    message += (tableModel.getValueAt(i,j)+"\n");
                }
            }
        }
        message +="Total Pendapatan : "+this.totalHarga;
        return message;
    }
